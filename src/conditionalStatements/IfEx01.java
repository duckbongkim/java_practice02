package conditionalStatements;

public class IfEx01 {
    public static void main(String[] args) {
        double num1 = 7.1;

        if(num1 <= 9){
            System.out.println("어바웃타임");
        }
        
        if(num1 <= 8){
            System.out.println("토이스토리");
        }
        
        if(num1 <= 7){
            System.out.println("고질라");
        }
    }
}
